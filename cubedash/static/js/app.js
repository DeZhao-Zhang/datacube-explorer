function set_onclick(button, i) {
  button.click(function () {
     datasets.clearLayers();
    $("#loading").show();
    $.getJSON("/api/datasets/ls8_nbar_albers/2013-"+i, function (data) {
      datasets.addData(data);
    $("#loading").hide();
    });
  })
}

